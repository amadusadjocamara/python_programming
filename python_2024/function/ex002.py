# this is exercise of to find the average max and min
ahsoka = 1.70
yoda = 0.66
r2d2 = 1.90
# average calculation
avag = (ahsoka + yoda + r2d2)/3
max_val = max(ahsoka,yoda,r2d2)
min_val = min(ahsoka,yoda,r2d2)

print(f"The average height is {avag} the maximum height {max_val} the minimum height is {min_val}")